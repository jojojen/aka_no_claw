def _coerce_manual_override(value: str | GrammarCardManualOverride) -> GrammarCardManualOverride:
    if isinstance(value, GrammarCardManualOverride):
        return value
    return GrammarCardManualOverride(explanation_zh=value)


def _structure_summary_zh(headword: str) -> str:
    if "連体修飾" in headword:
        return "這張卡練習連體修飾：修飾名詞的句子要放在名詞前，助詞和述語的搭配不能拆散。"
    if "引用" in headword:
        return "這張卡練習引用結構：先確認被引用的內容，再看它如何接到評價、否定或述語。"
    if "条件" in headword:
        return "這張卡練習條件節與主句的連接：先判斷條件範圍，再確認主句的述語。"
    if "目的語" in headword:
        return "這張卡練習目的語和述語的語順：目的語通常由「を」標示，需放在對應動詞前。"
    if "語順" in headword:
        return "這張卡練習日語語順：先找助詞標示的角色，再確認修飾語緊貼被修飾語。"
    if "名詞化" in headword:
        return "這張卡練習名詞化：用「の」等形式把動作或句子變成名詞成分，再接後項。"
    if "助詞" in headword:
        return "這張卡練習助詞功能：先確認助詞標示的角色，再判斷它和後面述語的關係。"
    return "這張卡練習句子結構判斷：先找核心述語，再確認修飾語、助詞與前後句的連接。"


def _summary_from_headword_rule(headword: str) -> str | None:
    for marker, summary in _GRAMMAR_HEADWORD_SUMMARY_RULES:
        if marker in headword:
            return summary
    if any(marker in headword for marker in _STRUCTURAL_HEADWORD_MARKERS):
        return _structure_summary_zh(headword)
    return None


def get_grammar_card_manual_override(headword: str) -> GrammarCardManualOverride | None:
    key = (headword or "").strip()
    if not key:
        return None
    explicit = GRAMMAR_CARD_MANUAL_OVERRIDES.get(key)
    if explicit is not None:
        return _coerce_manual_override(explicit)
    summary = _summary_from_headword_rule(key)
    if summary:
        return GrammarCardManualOverride(explanation_zh=summary)
    return GrammarCardManualOverride(
        explanation_zh=(
            f"這張卡練習「{key}」的用法。請先確認接續、助詞關係和前後句語意，再判斷句中功能。"
        )
    )
